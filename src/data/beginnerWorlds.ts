const createWords = (worldId: string, words: WordSeed[]): VocabularyWord[] =>
  words.map(([es, en, example], index) => ({
    id: `${worldId}-${slugify(es)}-${index + 1}`,
    es,
    en,
    example,
    tags: ["beginner"],
  }));

export const beginnerWorlds: World[] = seeds.map((world) => ({
  ...world,
  words: createWords(world.id, world.words),
}));
