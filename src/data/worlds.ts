const createWords = (
  worldId: string,
  sourcePage: number,
  words: WordSeed[],
): VocabularyWord[] =>
  words.map(([es, en, example], index) => ({
    id: `${worldId}-${slugify(es)}-${index + 1}`,
    es,
    en,
    example,
    sourcePage,
  }));

export const worlds: World[] = seeds.map(({ sourcePage, ...world }) => ({
  ...world,
  words: createWords(world.id, sourcePage, world.words),
}));

export const allWords = worlds.flatMap((world) => world.words);

export const getWorld = (worldId: string) =>
  worlds.find((world) => world.id === worldId);
